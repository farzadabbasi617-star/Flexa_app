/** Core pricing philosophy shared across all games. */
const CORE_LOGIC = `
# منطق پایه قیمت‌گذاری (برای همه بازی‌ها)

قیمت نهایی = ( ارزش پایه از لول و پیشرفت رایگان
            + ارزش افزاینده‌ی آیتم‌های پولی و کمیاب )
          × ضریب کمیابی
          × ضریب امنیت/ریجن/سیو
          × ضریب بازار روز
          − کارمزد

قواعد مهم:
- آیتم‌های دائمی، پولی و کمیاب بسیار مهم‌تر از لول و رنک هستند. لول و رنک فقط «پایه» می‌سازند.
- تفکیک «رایگان» از «غیررایگان/پولی» حیاتی است؛ آیتم پولی چند برابر نسخه رایگان ارزش دارد.
- کمیابی یک «ضریب» است نه «جمع ساده»؛ آیتم خیلی کمیاب می‌تواند کل قیمت را چند برابر کند.
- بازده نزولی (diminishing returns): وقتی تعداد یک آیتم خیلی زیاد می‌شود، ارزش هر واحد اضافه کمتر می‌شود.
- قیمتِ خرج‌شده ≠ قیمت فروش. افت دست‌دوم معمولاً ۵۰٪ تا ۷۰٪ است.
- خروجی باید واقع‌بینانه و مطابق بازار روز ایران باشد؛ نه خوش‌بینانه نه بدبینانه.
- بهتر است یک «بازه‌ی منصفانه» در ذهن داشته باشی و عددِ وسطِ منطقی را به‌عنوان قیمت بدهی.

# ضرایب امنیت و دسترسی (کاهنده‌ی قیمت)
- تک‌سیو امن / «جیمیل خام» = امن‌ترین حالت = ضریب ۱۰۰٪ (بالاترین قیمت).
- دو سیو ≈ کسر ۲۰٪ ؛ سه سیو یا بیشتر ≈ کسر ۳۰٪ تا ۵۰٪.
- ایمیل غیرقابل تغییر ≈ کسر ۴۰٪ تا ۴۵٪.
- سابقه بن، شارژ غیرقانونی یا اکانت مشکوک = عملاً غیرقابل فروش / قیمت بسیار پایین.

# کارمزد (مرجع بازار)
- تک‌سیو حدود ۵٪، دوسیو حدود ۱۰٪، و به‌ازای هر سیو اضافه حدود ۲.۵٪ بیشتر.

# نکات تکمیلی برای واقع‌بینانه بودن
- قیمت آگهی معمولاً بالاتر از قیمت فروش واقعی است؛ کمی محافظه‌کارانه قیمت بده.
- هرچه قیمت کل بالاتر برود، نقدشوندگی پایین‌تر می‌آید؛ برای اکانت‌های خیلی گران کمی تخفیف نقدشوندگی در نظر بگیر.
- آیتم‌های «ستاره‌ای و کمیاب» را پله‌ای/نمایی وزن بده، نه جمع ساده.
`.trim();

